const processCurriculumData = (data: any[]) => {
  const processed = [];
  let lastSubject = "";
  let lastUnit = "";
  for (const item of data) {
    const subject = item["ชื่อวิชา"] || lastSubject;
    const unit = item["หน่วย"] || lastUnit;
    if (subject) lastSubject = subject;
    if (unit) lastUnit = unit;
    if (item["เรื่อง"]) processed.push({ "ชื่อวิชา": subject, "หน่วย": unit, "เรื่อง": item["เรื่อง"] });
  }
  return processed;
};

